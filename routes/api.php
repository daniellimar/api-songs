<?php

use Illuminate\Support\Facades\Route;

use App\Http\Controllers\Api\MusicController;
use App\Http\Controllers\Api\MusicUploadController;

/*
|--------------------------------------------------------------------------
| Music Upload
|--------------------------------------------------------------------------
*/

Route::post(
    '/musics/upload',
    [MusicUploadController::class, 'upload']
);

/*
|--------------------------------------------------------------------------
| Musics CRUD
|--------------------------------------------------------------------------
*/

Route::prefix('musics')
    ->controller(MusicController::class)
    ->group(function () {

        /*
        |--------------------------------------------------------------------------
        | Listar músicas
        |--------------------------------------------------------------------------
        */

        Route::get(
            '/',
            'index'
        );

        /*
        |--------------------------------------------------------------------------
        | Streaming
        |--------------------------------------------------------------------------
        */

        Route::get(
            '/{music}/stream',
            'stream'
        );

        /*
        |--------------------------------------------------------------------------
        | Buscar música
        |--------------------------------------------------------------------------
        */

        Route::get(
            '/{music}',
            'show'
        );

        /*
        |--------------------------------------------------------------------------
        | Atualizar música
        |--------------------------------------------------------------------------
        */

        Route::put(
            '/{music}',
            'update'
        );

        /*
        |--------------------------------------------------------------------------
        | Remover música
        |--------------------------------------------------------------------------
        */

        Route::delete(
            '/{music}',
            'destroy'
        );
    });
